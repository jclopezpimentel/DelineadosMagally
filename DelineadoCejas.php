				<p class="titulo"><a name="tema4"> ¿Qué es el delineado de ojos?</a></p>
			</div>

			<div class="info">
				<p class="info">
                <pre>
                
     El delineado de ojos Permanentes se realizara según la forma de tu rostro y ojos, resaltará
     los rasgos que más te gusten y de paso disimularán aquellos “defectos” que no quieras destacar,
     Es un gran aliado para la mujer de hoy porque te olvidaras, si ya se corrió el delineador o 
     traes los ojos todos manchados, donde podras nadar, correr, sudar y tus ojos siempre lucirán 
     perfectos, mostrando siempre un buen aspecto con una mirada muy expresiva alegre y joven.
     
      <h3>            Iluminadora o linea bicolor</h3>
             
      La línea de iluminación o bicolor es especial para la mujeres de hoy que quieran agrandar o 
      darle mas expresión a sus ojos, con la iluminadora o bicolor se acaban los ojos tristes o con
      poca luz, ya que al utilizar pigmentos claros en el parpado superior le otorga al ojo alegría 
      y juventud, en donde podemos resaltar el color natural de  de tus ojos, se puede realizar líneas
      muy delgadas solo para darles luz o líneas gruesas o medias según tu estilo o tipo de ojos. 
      Llevar un delineado con la iluminación te veras siempre arreglada las 24 hrs.
            
